export const createProductController = async (req, res) => {
  console.log("Create Product Controller");
  try {
    const { name, description, price, category, quantity, shipping, isChecked } =
      req.fields;
    const { photo, model3D } = req.files;
    //alidation
    switch (true) {
      case !name:
        return res.status(500).send({ error: "Name is Required" });
      case !description:
        return res.status(501).send({ error: "Description is Required" });
      case !price:
        return res.status(502).send({ error: "Price is Required" });
      case !category:
        return res.status(503).send({ error: "Category is Required" });
      case !quantity:
        return res.status(504).send({ error: "Quantity is Required" });
      case isChecked && !model3D:
        return res.status(505).send({ error: "3D Model file is required" });

      case photo && photo.size > 1000000:
        return res
          .status(507)
          .send({ error: "photo is Required and should be less then 1mb" });

      case model3D && model3D.size > 10000000:
        return res
          .status(506)
          .send({ error: "3D model file should be less than 10MB" });

    }



    const products = new productModel({ ...req.fields, slug: slugify(name) });
    if (photo) {
      products.photo.data = fs.readFileSync(photo.path);
      products.photo.contentType = photo.type;
    }
    if (model3D) {
      products.model3D.data = fs.readFileSync(model3D.path);
      products.model3D.contentType = model3D.type;
    }
    else {
      console.log("No 3D Model");
    }

    await products.save();

    res.status(201).send({
      success: true,
      message: "Product Created Successfully",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in crearing product",
    });
  }
};

